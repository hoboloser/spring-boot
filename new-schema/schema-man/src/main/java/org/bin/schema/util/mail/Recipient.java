package org.bin.schema.util.mail;

public class Recipient {

	/**
	 * 收件人地址
	 */
	private String mail;
	
	/**
	 * 名字
	 */
	private String name;

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
